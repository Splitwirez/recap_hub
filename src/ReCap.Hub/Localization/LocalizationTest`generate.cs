        {
            var keys = _PSEUDO_CHARS_IN.Keys;
            List<string> lines = new();
            foreach (var inKey in keys)
            {
                string outKey = _PSEUDO_CHARS_IN[inKey];
                if (outKey.Length > 1)
                    continue;
                
                lines.Add("            {");

                lines.Add($"                '{outKey}',");
                
                string outValue = inKey;
                string outValue2 = string.Empty;
                for (int i = 0; i < outValue.Length; i++)
                {
                    outValue2 += $"\"{outValue[i]}\", ";
                }
                lines.Add("                new[] { " + outValue2 + "} ");
                //string line = "            { \"{outKey}\", \"{outValue}\"},";
                //lines.Add(line);
                
                
                lines.Add("            },");
            }
            
            string outPath = Path.Combine(
                Environment.GetEnvironmentVariable(ReCap.CommonUI.OSInfo.IsWindows ? "USERPROFILE" : "HOME"),
                "localization-dictionary.txt"
            );
            File.WriteAllLines(
                outPath
                , lines);
        }
